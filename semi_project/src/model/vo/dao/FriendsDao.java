package model.vo.dao;

public class FriendsDao {
	String friendId;
	String friendName;
	String friendNick;
	String friendMsg;
	String friendMsgDate;
	public String getFriendId() {
		return friendId;
	}
	public void setFriendId(String friendId) {
		this.friendId = friendId;
	}
	public String getFriendName() {
		return friendName;
	}
	public void setFriendName(String friendName) {
		this.friendName = friendName;
	}
	public String getFriendNick() {
		return friendNick;
	}
	public void setFriendNick(String friendNick) {
		this.friendNick = friendNick;
	}
	public String getFriendMsg() {
		return friendMsg;
	}
	public void setFriendMsg(String friendMsg) {
		this.friendMsg = friendMsg;
	}
	public String getFriendMsgDate() {
		return friendMsgDate;
	}
	public void setFriendMsgDate(String friendMsgDate) {
		this.friendMsgDate = friendMsgDate;
	}
	
	
}
