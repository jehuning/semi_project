package model.vo.dao;

public class UserProfileDao {
	private String pic_Name;
	private String pic_RealName;
	private String profile_txt;
	private String profile_date;
	
	public String getPic_Name() {
		return pic_Name;
	}
	public void setPic_Name(String pic_Name) {
		this.pic_Name = pic_Name;
	}
	public String getPic_RealName() {
		return pic_RealName;
	}
	public void setPic_RealName(String pic_RealName) {
		this.pic_RealName = pic_RealName;
	}
	public String getProfile_txt() {
		return profile_txt;
	}
	public void setProfile_txt(String profile_txt) {
		this.profile_txt = profile_txt;
	}
	public String getProfile_date() {
		return profile_date;
	}
	public void setProfile_date(String profile_date) {
		this.profile_date = profile_date;
	}
	
	
	
	
}
